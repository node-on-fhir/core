// scripts/generate-fhir-schema-index.js
// Regenerates imports/lib/schemas/R4B/JsonSchema/index.js from the *.json
// files in that directory. Run after adding/refreshing schemas
// (see /fetch-fhir-schemas).
const fs = require('fs');
const path = require('path');

const dir = path.join(__dirname, '..', 'imports', 'lib', 'schemas', 'R4B', 'JsonSchema');
const files = fs.readdirSync(dir).filter(function(f) { return f.endsWith('.json'); }).sort();

if (files.length === 0) {
  console.error('[generate-fhir-schema-index] no .json schemas found in', dir);
  process.exit(1);
}

const lines = files.map(function(f) {
  const name = path.basename(f, '.json');
  return "  '" + name + "': require('./" + f + "'),";
});

const out = [
  '// imports/lib/schemas/R4B/JsonSchema/index.js',
  '// GENERATED by scripts/generate-fhir-schema-index.js -- do not edit by hand.',
  'module.exports = {',
  lines.join('\n'),
  '};',
  ''
].join('\n');

fs.writeFileSync(path.join(dir, 'index.js'), out);
console.log('[generate-fhir-schema-index] wrote index.js with', files.length, 'schemas');
