// certification/select-inferno-report.js
// Stages the newest Inferno (g)(10) report for the Software Manual build.
//
// Scans certification/inferno-reports/*.pdf, splits the files into the
// summary and Detailed editions Inferno exports, and picks the newest of
// each by the session date embedded in the filename (YYYYMMDD or
// YYYY-MM-DD; file mtime as fallback). The winning summary is copied to
// the canonical staged name latest-g10-summary.pdf — LaTeX cannot cope
// with the spaces and parentheses in Inferno's default filenames, and the
// staged copy keeps manual-content.tex static across sessions. Alongside
// it, inferno-report-meta.tex records the real archived filenames and the
// session date so the appendix prose cites repo-derived facts.
//
// Both outputs are gitignored build intermediates; run via
// build-letter-edition.sh / build-scroll-edition.sh.
//
// Usage: node select-inferno-report.js

const fs = require('fs');
const path = require('path');

const REPORT_DIR = path.join(__dirname, 'inferno-reports');
const STAGED_PDF = path.join(REPORT_DIR, 'latest-g10-summary.pdf');
const META_TEX = path.join(__dirname, 'inferno-report-meta.tex');

const MONTHS = ['January', 'February', 'March', 'April', 'May', 'June',
  'July', 'August', 'September', 'October', 'November', 'December'];

function filenameDate(name) {
  const m = name.match(/(20\d{2})-?(\d{2})-?(\d{2})/);
  if (!m) {
    return null;
  }
  const year = Number(m[1]);
  const month = Number(m[2]);
  const day = Number(m[3]);
  if (month < 1 || month > 12 || day < 1 || day > 31) {
    return null;
  }
  return { year: year, month: month, day: day, sortKey: year * 10000 + month * 100 + day };
}

// Escape LaTeX special characters for use in text mode. Single pass so a
// replacement is never re-processed by a later rule (sequential .replace
// calls would mangle the braces/backslash inside \textbackslash{}).
function texEscape(value) {
  return String(value).replace(/[\\#$%&_{}~^]/g, function(ch) {
    if (ch === '\\') { return '\\textbackslash{}'; }
    if (ch === '~') { return '\\textasciitilde{}'; }
    if (ch === '^') { return '\\textasciicircum{}'; }
    return '\\' + ch;
  });
}

if (!fs.existsSync(REPORT_DIR)) {
  console.error('[select-inferno-report] missing directory: ' + REPORT_DIR);
  process.exit(1);
}

const candidates = fs.readdirSync(REPORT_DIR)
  .filter(function(name) {
    return /\.pdf$/i.test(name) && name !== path.basename(STAGED_PDF);
  })
  .map(function(name) {
    const stat = fs.statSync(path.join(REPORT_DIR, name));
    const date = filenameDate(name);
    return {
      name: name,
      detailed: /detailed/i.test(name),
      date: date,
      // date in the filename wins; mtime only breaks ties / covers undated files
      sortKey: date ? date.sortKey : 0,
      mtime: stat.mtimeMs
    };
  });

function newest(list) {
  return list.slice().sort(function(a, b) {
    return (b.sortKey - a.sortKey) || (b.mtime - a.mtime);
  })[0] || null;
}

const summary = newest(candidates.filter(function(c) { return !c.detailed; }));
const detailed = newest(candidates.filter(function(c) { return c.detailed; }));

if (!summary) {
  console.error('[select-inferno-report] no summary report PDF found in ' + REPORT_DIR);
  console.error('[select-inferno-report] export the Inferno session report (non-Detailed) and drop it there.');
  process.exit(1);
}

fs.copyFileSync(path.join(REPORT_DIR, summary.name), STAGED_PDF);

// Measure the report's page size (first /MediaBox in the file) so the scroll
// edition can scale its pages to the scroll width while keeping the aspect
// ratio. Letter (612x792) is the fallback if the box isn't in plain text.
function pageBox(pdfPath) {
  const raw = fs.readFileSync(pdfPath).toString('latin1');
  const m = raw.match(/\/MediaBox\s*\[\s*([\d.+-]+)\s+([\d.+-]+)\s+([\d.+-]+)\s+([\d.+-]+)\s*\]/);
  if (!m) {
    console.warn('[select-inferno-report] no plain-text MediaBox found; assuming letter pages');
    return { width: 612, height: 792 };
  }
  return {
    width: Math.round(Math.abs(Number(m[3]) - Number(m[1]))),
    height: Math.round(Math.abs(Number(m[4]) - Number(m[2])))
  };
}
const box = pageBox(STAGED_PDF);

const dateText = summary.date
  ? MONTHS[summary.date.month - 1] + ' ' + summary.date.day + ', ' + summary.date.year
  : 'undated (filename carries no YYYYMMDD session date)';

const lines = [
  '% certification/inferno-report-meta.tex',
  '% GENERATED by select-inferno-report.js — do not edit; gitignored.',
  '\\newcommand{\\InfernoReportSourceFile}{' + texEscape(summary.name) + '}',
  '\\newcommand{\\InfernoReportDate}{' + texEscape(dateText) + '}',
  '\\newcommand{\\InfernoDetailedSourceFile}{' +
    (detailed ? texEscape(detailed.name) : 'none archived') + '}',
  '% Report page box in PDF points (integers, for \\dimexpr ratios)',
  '\\newcommand{\\InfernoReportPageWidthPt}{' + box.width + '}',
  '\\newcommand{\\InfernoReportPageHeightPt}{' + box.height + '}',
  ''
];
fs.writeFileSync(META_TEX, lines.join('\n'));

console.log('[select-inferno-report] staged summary: ' + summary.name + ' (' + dateText + ')');
console.log('[select-inferno-report] detailed archive: ' + (detailed ? detailed.name : 'none'));
console.log('[select-inferno-report] wrote ' + path.basename(STAGED_PDF) + ' + ' + path.basename(META_TEX));
